                    <p>Pakistan Railways provide an important mode of Transportation in the farthest corners of the country and 
                        brings them closer for Business, sight seeing, pilgrimage and education. It has been a great integrating 
                        force and forms the life line of the country by catering to its needs for large scale movement of people 
                        and freight.</p>

                    <p>The possibility of Karachi as a sea port was first noticed in the mid of 19th century and Sir Henry Edward 
                        Frere who was appointed Commissioner of Sindh after its annexation with Bombay in 1847 sought permission 
                        from Lord Dalhousie to begin survey of sea port. He also initiated the survey for Railway line in 1858 . 
                        It was proposed that a railway line from Karachi City to Kotri, steam navigation up the Indus /Chenab upto 
                        Multan and from there another railway line to Lahore and beyond be constructed.</p>

                    <p>It was on 13th May,1861 that first railway line was opened for public traffic between Karachi City and Kotri, 
                        the distance of 105 miles. The line between Karachi City and Kiamari was opened on 16.6.1889. By 1897 the 
                        line from Kiamari to Kotri was doubled.</p>

                    <p>The railway line from Peshawar to Karachi closely follows Alexander’s line of march through the Hindu Kush 
                        to the sea. Different sections on existing main line from Peshawar to Lahore and Multan and branch lines 
                        were constructed in the last quarter of 19th century and early years of 20th century.</p>

                    <p>The 4 sections i.e. Scinde Railways, Indian Flotilla Company, Punjab Railway and Delhi Railways working in 
                        a single company were later on amalgamated into Scinde, Punjab &amp; Delhi Railways Company and was purchased 
                        by the Secretary of State for India in 1885 and in January, 1886 it was named North Western State Railways 
                        which was later on renamed as North Western Railways.</p>

                    <p>At the time of partition, North Western Railways 1847 route mile was transferred to India leaving route 
                        miles 5048 to Pakistan. In 1954 The railway line was extended to Mardan and Charsada section and in 1956 
                        Jacababad-Kashmore 2’-6’’ line was converted into broad gauge. Kot Adu-Kashmore line was constructed between 
                        1969 to 1973 providing an alternate route from Karachi to up country.</p>
